The Kubernetes Serverless CLI (kslctl) Project provides a unifying command-line interface for interacting with the Kubernetes Serverless Stack which constitutes of Knative, Tekton and ArgoCD.

Usage:
  kb8lctl [command]

Available Commands:
  Get         description of the command
  completion  Generate the autocompletion script for the specified shell
  help        Help about any command

Flags:
  -h, --help     help for kb8lctl
  -t, --toggle   Help message for toggle

Use "kb8lctl [command] --help" for more information about a command.